    private void btnCapNhatBangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatBangActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            ArrayList<HoaDonThue> listT = hdtc.getListHoaDonThue();
            for(HoaDonThue t : listT){
                String maThue = t.getMaThue();
                String size = t.getSize();
                int soLuong = t.getsL();
                SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
                Date d1 = new Date();
                Date d2 = null;
                try {
                    d2 = sdf.parse(t.getNgayTra());
                } catch (ParseException e) {
                }
                long diff = d2.getTime() - d1.getTime();
                if(diff <= 0 && t.isCheckTra() != true){
                    DongPhucController.congDPTon(size, soLuong);
                    HoaDonThueController.checkTraTrue(maThue);
                }
                
            }
            fillTable();
        } catch (IOException ex) {
        }
    }//GEN-LAST:event_btnCapNhatBangActionPerformed

    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        String maThue = txtTimKiem.getText();
        try {
            tblModel.setRowCount(0);
            HoaDonThue hdt = hdtc.getHoaDonThueByMaThue(maThue);
            if (hdt == null) {
                JOptionPane.showMessageDialog(rootPane, "Đơn hàng không hợp lệ!", "Thông báo", JOptionPane.CLOSED_OPTION);
                return;
            }
            tblModel.addRow(new Object[]{hdt.getMaThue(),hdt.getSize(), hdt.getsL(), hdt.getMaSinhVien(), hdt.getNgayThue(), hdt.getNgayTra(), hdt.getDonGia(), hdt.tongTien(), hdt.isCheckTra()});
            tblModel.fireTableDataChanged();
            } catch (IOException ex) {
            }
    }//GEN-LAST:event_btnTimKiemActionPerformed

