    private void btnXoaDonThueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaDonThueActionPerformed
        // TODO add your handling code here:
        int removeIndex = tbDonThue.getSelectedRow();
        if(removeIndex == -1)
            JOptionPane.showMessageDialog(rootPane, "Chưa chọn dòng cần xóa!", "Thông báo", JOptionPane.CLOSED_OPTION);
        else {
            int choice = JOptionPane.showConfirmDialog(this,
                "Xác nhận xóa đơn hàng?",
                "Hỏi", JOptionPane.YES_NO_OPTION);
            if (choice == JOptionPane.YES_OPTION) {
                int row = tbDonThue.getSelectedRow();
                String maThue = tbDonThue.getModel().getValueAt(row, 0).toString();
                String size = tbDonThue.getModel().getValueAt(row, 1).toString();
                String soLuong = tbDonThue.getModel().getValueAt(row, 2).toString();
                String han = tbDonThue.getModel().getValueAt(row, 8).toString();
                int modelIndex = tbDonThue.convertRowIndexToModel(removeIndex);
                DefaultTableModel model = (DefaultTableModel)tbDonThue.getModel();
                model.removeRow(modelIndex);
                try {
                    hdtc.xoaHDThue(maThue);
                    if(han != "true")
                        DongPhucController.congDPTon(size, Integer.parseInt(soLuong));
                } catch (IOException ex) {
                }
                JOptionPane.showMessageDialog(null, "Bạn xóa thành công!", "Thông báo", JOptionPane.CLOSED_OPTION);
            }
        }
    }//GEN-LAST:event_btnXoaDonThueActionPerformed

